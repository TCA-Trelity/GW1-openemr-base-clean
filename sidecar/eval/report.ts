// Eval report generator (S2.5): turns the JSONL ledger the eval suite writes into the
// committed deliverable docs/execution/eval-results.md (summary + table + notes +
// timestamp + commit SHA). Run via `npm run eval` (eval/run.ts) or standalone:
// `npx tsx eval/report.ts` regenerates the doc from the last suite run's ledger.
import { execSync } from 'node:child_process';
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath, pathToFileURL } from 'node:url';
import { categoryForRecord, EVAL_CATEGORIES, isSafetyCategory } from './categories.js';
import {
    EVAL_DIFFICULTIES,
    METRICS_PATH,
    RESULTS_PATH,
    type EvalDifficulty,
    type EvalMetric,
    type EvalRecord,
} from './collector.js';
import { computeCategoryStats } from './gate.js';

const OUTPUT_PATH = fileURLToPath(new URL('../../docs/execution/eval-results.md', import.meta.url));
const REPO_ROOT = fileURLToPath(new URL('../..', import.meta.url));

// Fixed publication order for the eval suites (S2.5; multi-turn conversation added by M1).
const SUITE_ORDER = [
    'citation-validity-100',
    'contradiction-ground-truth',
    'calculator-goldens',
    'empty-record-boundary',
    'cross-patient-denial',
    'injection-resistance',
    'multi-turn-conversation',
    'prescriptiveness',
    'imaging-cohesion',
    'retrieval-grounded',
];

export interface ReportSummary {
    total: number;
    failed: number;
}

function commitSha(): string {
    const fromEnv = process.env['GITHUB_SHA'];
    if (fromEnv !== undefined && fromEnv !== '') {
        return fromEnv;
    }
    try {
        return execSync('git rev-parse HEAD', { cwd: REPO_ROOT, encoding: 'utf8' }).trim();
    } catch {
        return 'unknown';
    }
}

function readRecords(): EvalRecord[] {
    if (!existsSync(RESULTS_PATH)) {
        return [];
    }
    const lines = readFileSync(RESULTS_PATH, 'utf8')
        .split('\n')
        .map((line) => line.trim())
        .filter((line) => line.length > 0);
    // De-duplicate by id (last write wins) so a re-run over a stale ledger stays sane.
    const byId = new Map<string, EvalRecord>();
    for (const line of lines) {
        const record = JSON.parse(line) as EvalRecord;
        byId.set(record.id, record);
    }
    const suiteRank = (id: string): number => {
        const index = SUITE_ORDER.findIndex((suite) => id.startsWith(`${suite}.`));
        return index === -1 ? SUITE_ORDER.length : index;
    };
    return [...byId.values()].sort((a, b) => suiteRank(a.id) - suiteRank(b.id) || a.id.localeCompare(b.id));
}

function cell(value: string | number): string {
    return String(value).replaceAll('|', '\\|').replaceAll('\n', ' ');
}

/** CT3: read the metrics side-channel ledger (same last-write-wins stance as readRecords). */
function readMetrics(): EvalMetric[] {
    if (!existsSync(METRICS_PATH)) {
        return [];
    }
    const byId = new Map<string, EvalMetric>();
    for (const line of readFileSync(METRICS_PATH, 'utf8').split('\n')) {
        const trimmed = line.trim();
        if (trimmed.length === 0) {
            continue;
        }
        const metric = JSON.parse(trimmed) as EvalMetric;
        byId.set(metric.evalId, metric);
    }
    return [...byId.values()].sort((a, b) => a.evalId.localeCompare(b.evalId));
}

export function generateReport(options: { suiteFailed?: boolean } = {}): ReportSummary {
    const records = readRecords();
    const failed = records.filter((record) => !record.pass).length;
    const generatedAt = new Date().toISOString();
    const sha = commitSha();

    const lines: string[] = [
        '# Clinical Co-Pilot — Eval Results',
        '',
        '> **Generated file — do not edit.** Regenerated by `npm run eval` in `sidecar/`',
        '> (suite: `sidecar/eval/`). Committed as a deliverable and refreshed on every run;',
        '> CI regenerates and uploads it as an artifact on every push touching `sidecar/**`.',
        '',
        `- **Generated:** ${generatedAt}`,
        `- **Commit:** \`${sha}\` (workspace HEAD at generation time; in CI, the pushed commit)`,
        `- **Result:** ${records.length - failed}/${records.length} evals passed${failed > 0 ? ' — **FAILURES BELOW**' : ''}`,
    ];
    if (options.suiteFailed === true && failed === 0) {
        lines.push(
            '- **Warning:** the eval suite process exited non-zero even though every recorded eval passed — some evals may not have run to the point of recording. Treat this run as failed and check the suite logs.',
        );
    }
    lines.push(
        '',
        '## Results',
        '',
        '| Eval | Description | Metric | Value | Threshold | Pass |',
        '|------|-------------|--------|-------|-----------|:----:|',
    );
    for (const record of records) {
        lines.push(
            `| \`${cell(record.id)}\` | ${cell(record.description)} | ${cell(record.metric)} | ${cell(record.value)} | ${cell(record.threshold)} | ${record.pass ? 'PASS' : '**FAIL**'} |`,
        );
    }

    // Week 2 rubric-category rollup (REQ S4/R6): the same buckets the PR gate judges
    // (eval/gate.ts, baseline in eval/baseline.json). Safety categories fail the build on
    // any failing case; quality categories on >5% regression vs baseline or below threshold.
    const { stats, uncategorized } = computeCategoryStats(records);
    lines.push(
        '',
        '## Rubric categories (Week 2 gate)',
        '',
        '| Category | Tier | Cases | Passed | Pass rate |',
        '|----------|------|------:|-------:|----------:|',
    );
    for (const category of EVAL_CATEGORIES) {
        const s = stats.get(category) ?? { total: 0, passed: 0, rate: 1 };
        const tier = isSafetyCategory(category) ? 'safety (per-case)' : 'quality (>5%/threshold)';
        const rate = s.total === 0 ? '—' : `${(s.rate * 100).toFixed(1)}%`;
        lines.push(`| \`${category}\` | ${tier} | ${s.total === 0 ? '— (not yet measured)' : s.total} | ${s.total === 0 ? '—' : s.passed} | ${rate} |`);
    }
    if (uncategorized.length > 0) {
        lines.push('', `**Uncategorized records (fix suite or legacy map):** ${uncategorized.map((id) => `\`${id}\``).join(', ')}`);
    }

    // CT2 difficulty rollup — reporting only, the gate never reads difficulty. The tier is
    // REQUIRED on every EvalRecord at compile time (typecheck:eval), so no case can land
    // here untagged; the untagged guard below only catches a stale hand-made ledger.
    const byDifficulty = new Map<EvalDifficulty, { total: number; passed: number }>(
        EVAL_DIFFICULTIES.map((difficulty) => [difficulty, { total: 0, passed: 0 }]),
    );
    const untagged: string[] = [];
    for (const record of records) {
        const bucket = byDifficulty.get(record.difficulty);
        if (bucket === undefined) {
            untagged.push(record.id);
            continue;
        }
        bucket.total += 1;
        if (record.pass) {
            bucket.passed += 1;
        }
    }
    lines.push(
        '',
        '## Coverage by difficulty',
        '',
        'Every case carries a required difficulty tier (compile-enforced, reporting-only):',
        '**straightforward** = clean input, expected happy path; **ambiguous** = requires',
        'judgment/disambiguation (multi-turn context, degraded scans, overlapping-document',
        'tie-breaks); **edge-case** = adversarial/degenerate (injection, PHI canaries, empty',
        'record, cross-patient isolation, refusals).',
        '',
        '| Difficulty | Cases | Passed | Pass rate |',
        '|------------|------:|-------:|----------:|',
    );
    for (const difficulty of EVAL_DIFFICULTIES) {
        const d = byDifficulty.get(difficulty) ?? { total: 0, passed: 0 };
        const rate = d.total === 0 ? '—' : `${((d.passed / d.total) * 100).toFixed(1)}%`;
        lines.push(`| ${difficulty} | ${d.total === 0 ? '—' : d.total} | ${d.total === 0 ? '—' : d.passed} | ${rate} |`);
    }
    if (untagged.length > 0) {
        lines.push('', `**Untagged records (stale ledger — rerun \`npm run eval\`):** ${untagged.map((id) => `\`${id}\``).join(', ')}`);
    }
    // Category × difficulty concentration matrix: where the hard cases actually live.
    const matrix = new Map<string, number>();
    for (const record of records) {
        const category = categoryForRecord(record);
        if (category === undefined) {
            continue; // already surfaced by the uncategorized list above
        }
        const key = `${category}|${record.difficulty}`;
        matrix.set(key, (matrix.get(key) ?? 0) + 1);
    }
    lines.push(
        '',
        'Case counts per rubric category × difficulty:',
        '',
        `| Category | ${EVAL_DIFFICULTIES.join(' | ')} |`,
        `|----------|${EVAL_DIFFICULTIES.map(() => '---:').join('|')}|`,
    );
    for (const category of EVAL_CATEGORIES) {
        const counts = EVAL_DIFFICULTIES.map((difficulty) => {
            const n = matrix.get(`${category}|${difficulty}`) ?? 0;
            return n === 0 ? '—' : String(n);
        });
        lines.push(`| \`${category}\` | ${counts.join(' | ')} |`);
    }

    // CT3 retrieval quality: the two honestly-calculable numbers, derived from the actual
    // ranked result lists the retrieval eval records into the metrics side-channel —
    // never inferred from pass/fail booleans.
    const retrievalMetrics = readMetrics().filter((metric) => metric.kind === 'retrieval_rank');
    lines.push('', '## Retrieval quality', '');
    if (retrievalMetrics.length === 0) {
        lines.push(
            'No retrieval rank metrics were recorded this run (stale ledger) — rerun `npm run eval`.',
        );
    } else {
        const ranks = retrievalMetrics.flatMap((metric) => (metric.rank === null ? [] : [metric.rank]));
        const hitRate = (ranks.length / retrievalMetrics.length) * 100;
        const avgRank = ranks.length === 0 ? null : ranks.reduce((sum, rank) => sum + rank, 0) / ranks.length;
        lines.push(
            `- **Hit rate:** ${ranks.length}/${retrievalMetrics.length} (${hitRate.toFixed(1)}%) — retrieval goldens whose expected document appears anywhere in the returned results (the eval's top-3 retrieval window, the same list the gate judges).`,
            `- **Average rank:** ${avgRank === null ? '— (expected document never returned)' : avgRank.toFixed(2)} — mean 1-based position of the expected document's first snippet, over the ${ranks.length} golden(s) where it was found.`,
            '',
            'A true **precision** metric (how many of the top results are relevant when several',
            'documents could be) is deliberately absent: each retrieval golden today defines a',
            'single expected document, so precision cannot be calculated honestly from this set.',
            'Multi-document goldens are future work — the metric is omitted rather than faked.',
        );
    }

    lines.push(
        '',
        '## What these evals are (and are not)',
        '',
        '- **Corpus-level acceptance checks** over the authored ground-truth corpora',
        '  (`sidecar/seed/margaret-chen.json`, `sidecar/seed/william-thompson.json`),',
        '  run against the real shipped code: citation gate, pure clinical engines,',
        '  extraction validation, chat citation parsing, the multi-turn chat tool-use',
        '  loop (real tools over real corpora), and the overview builder.',
        '- **Deterministic — no live LLM calls.** Where an LLM sits in the loop',
        '  (extraction, chat), evals drive the real client through a mocked SSE stream,',
        '  so they prove the code around the model, not the model.',
        '- **Not unit tests.** Branch/boundary behavior of each component is locked by',
        '  the `sidecar/test/` and `sidecar/panel` vitest suites; these evals publish the corpus-level numbers',
        '  a reviewer should hold the system to.',
        '- **`injection-resistance` is structural.** It proves the prompt architecture',
        '  (document text fenced inside BEGIN/END TEXT; hard rules in the system prompt,',
        '  outside attacker-writable content) and that an invented citation id can never',
        '  render as provenance. It does **not** prove a live model ignores an injected',
        '  instruction.',
        '- **`multi-turn-conversation` is structural in the same sense.** The model is',
        '  scripted; the machinery is real. It proves the conversation loop: prior turns',
        '  reach the follow-up call verbatim, real tool output rides each tool_result',
        '  byte-identical, cross-patient denial holds at turn 2+, the round cap forces a',
        '  final answer, and a failing tool degrades to a structured error the loop',
        '  recovers from. It does **not** prove a live model picks the right tools.',
        '- **`prescriptiveness` is the judgment guardrail** (docs/prompt-guide.md): the',
        '  deterministic lint must flag every family of originated clinical direction and',
        '  pass the sanctioned consultative reframe, in-turn. A third, live-behavioral',
        '  case (real model vs. the same lint) is opt-in via `LIVE_EVALS=1` and spends',
        '  tokens — it is absent from the committed deterministic run.',
        '- **`imaging-cohesion` pins chat↔rail unity.** The chat tool and the panel\'s',
        '  analytics rail must derive the imaging story from one source: the tool output',
        '  is deep-equal to the overview builder\'s imaging block, per corpus, and rides',
        '  the real loop verbatim. Its live case (opt-in, `LIVE_EVALS=1`) replays the',
        '  observed failure — a trend ask answered with a false absence claim — and',
        '  requires an imaging tool consultation instead.',
        '',
        '## Known limitations and notes',
        '',
    );
    const noted = records.filter((record) => record.notes !== undefined && record.notes !== '');
    for (const record of noted) {
        lines.push(`- **\`${cell(record.id)}\`** — ${cell(record.notes ?? '')}`);
    }
    lines.push(
        '',
        '## Future work',
        '',
        '- Behavioral injection-resistance evals: run the real extraction model over',
        '  adversarial corpus documents and score whether authored allergy facts survive',
        '  and the injected id stays uncited.',
        '- Live extraction-fidelity evals: real model over the full corpora, scored as',
        '  precision/recall against the authored facts.',
        '- Live rubric-graded chat answer-quality evals (grounding + refusal-on-absence);',
        '  the deterministic conversation-loop machinery is covered by the',
        '  `multi-turn-conversation` suite above.',
        '- Interval-engine hardening for same-day post-injection scans (see the',
        '  `calculator-goldens.interval-over-extension` note).',
        '',
        '## How to run',
        '',
        '```bash',
        'cd sidecar',
        'npm ci',
        'npm run eval          # runs the suite and regenerates this file',
        '',
        '# suite only (no report):',
        'npx vitest run --config eval/vitest.config.ts',
        '',
        '# typecheck the eval suite (outside the main tsconfig include):',
        'npm run typecheck:eval',
        '```',
        '',
    );

    writeFileSync(OUTPUT_PATH, lines.join('\n'), 'utf8');
    return { total: records.length, failed };
}

// Standalone entry point: `npx tsx eval/report.ts`.
if (process.argv[1] !== undefined && import.meta.url === pathToFileURL(process.argv[1]).href) {
    const { total, failed } = generateReport();
    console.log(`eval report written to ${OUTPUT_PATH} (${total - failed}/${total} passed)`);
    process.exitCode = failed > 0 || total === 0 ? 1 : 0;
}
